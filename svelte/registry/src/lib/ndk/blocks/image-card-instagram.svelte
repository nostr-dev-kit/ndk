<!--
  @component ImageCardInstagram
  Instagram-style image card with user header, square image, caption, and actions.
  Classic social feed layout optimized for image content.

  @example
  ```svelte
  <ImageCardInstagram {ndk} {image} />
  <ImageCardInstagram {ndk} {image} showDropdown={false} />
  ```
-->
<script lang="ts">
	import type { NDKImage } from '@nostr-dev-kit/ndk';
	import type { NDKSvelte } from '@nostr-dev-kit/svelte';
	import { UserProfile } from '../user-profile';
	import { getNDKFromContext } from '../ndk-context.svelte.js';
	import RepostButton from './repost-button.svelte';
	import { ReactionButton } from './index.js';
	// import { ZapButton } from '../zap/index.js'; // TODO: Add ZapButton when available
	import { cn } from '$lib/utils';

	interface Props {
		/** NDK instance */
		ndk?: NDKSvelte;

		/** The image event to display */
		image: NDKImage;

		/** Show dropdown menu */
		showDropdown?: boolean;

		/** Additional CSS classes */
		class?: string;
	}

	let {
		ndk: providedNdk,
		image,
		showDropdown = true,
		class: className = ''
	}: Props = $props();

	const ndk = getNDKFromContext(providedNdk);

	// Get first imeta image
	const imeta = $derived(image.imetas[0]);
	const imageUrl = $derived(imeta?.url);
	const caption = $derived(image.content);
</script>

<div class={cn('instagram-card', className)}>
	<!-- Header -->
	<div class="instagram-header">
		<UserProfile.Root {ndk} pubkey={image.pubkey}>
			<div class="instagram-user-info">
				<UserProfile.Avatar class="w-10 h-10" />
				<div class="flex-1">
					<UserProfile.Name class="font-semibold text-sm" />
					<UserProfile.RelativeTime event={image} class="text-xs text-muted-foreground" />
				</div>
			</div>
		</UserProfile.Root>

		{#if showDropdown}
			<button class="instagram-menu-btn" aria-label="Options">
				<svg width="20" height="20" fill="currentColor" viewBox="0 0 24 24">
					<circle cx="12" cy="12" r="2" />
					<circle cx="19" cy="12" r="2" />
					<circle cx="5" cy="12" r="2" />
				</svg>
			</button>
		{/if}
	</div>

	<!-- Image -->
	<div class="instagram-image-container">
		{#if imageUrl}
			<img src={imageUrl} alt={imeta?.alt || 'Image'} class="instagram-image" loading="lazy" />
		{:else}
			<div class="instagram-image-placeholder">
				<span class="text-4xl text-muted-foreground">ðŸ“·</span>
			</div>
		{/if}
	</div>

	<!-- Actions -->
	<div class="instagram-actions">
		<ReactionButton {ndk} event={image} />
		<RepostButton {ndk} event={image} />
		<!-- <ZapButton {ndk} event={image} /> -->
	</div>

	<!-- Caption -->
	{#if caption}
		<div class="instagram-caption">
			<UserProfile.Root {ndk} pubkey={image.pubkey}>
				<UserProfile.Name class="font-semibold text-sm inline" />
			</UserProfile.Root>
			<span class="text-sm ml-2">{caption}</span>
		</div>
	{/if}
</div>

